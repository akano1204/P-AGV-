CREATE TABLE AGV_WORK_HISTORY(
	
	ID INT IDENTITY(1,1) NOT NULL,
	MAKE_DATE CHAR(8) NOT NULL DEFAULT '',
	ORDER_NO INT NOT NULL DEFAULT 0,
	ORDER_SUB_NO INT NOT NULL DEFAULT 0,
	AGV_ID NVARCHAR(10) NOT NULL DEFAULT '',
	START_TIME DATETIME NOT NULL DEFAULT '',
	END_TIME DATETIME DEFAULT NULL,
	FROM_QR NVARCHAR(20) NOT NULL DEFAULT '',
	TO_QR NVARCHAR(20) NOT NULL DEFAULT '',
	RACK_ID NVARCHAR(20) NOT NULL DEFAULT '',
	MAKE_TIME DATETIME NOT NULL DEFAULT GETDATE(),
	LAST_TIME DATETIME NOT NULL DEFAULT GETDATE(),
	
	CONSTRAINT AGV_WORK_HISTORY_PK PRIMARY KEY(ID))

CREATE INDEX
	AGV_WORK_HISTORY_INDEX01 ON AGV_WORK_HISTORY
	(ORDER_NO, ORDER_SUB_NO)

CREATE INDEX
	AGV_WORK_HISTORY_INDEX02 ON AGV_WORK_HISTORY
	(MAKE_DATE)

CREATE INDEX
	AGV_WORK_HISTORY_INDEX03 ON AGV_WORK_HISTORY
	(AGV_ID)
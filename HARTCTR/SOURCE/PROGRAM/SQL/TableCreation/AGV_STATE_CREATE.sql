CREATE TABLE AGV_STATE(

	AGV_ID NVARCHAR(10) NOT NULL,
	COMMAND_TIME DATETIME NOT NULL DEFAULT GETDATE(),
	ACTION CHAR(1) NOT NULL DEFAULT '',
	ORDER_NO INT NOT NULL DEFAULT 0,
	ORDER_SUB_NO INT NOT NULL DEFAULT 0,
	BATTERY INT NOT NULL DEFAULT 0,
	BATTERY_STATE INT NOT NULL DEFAULT 0,
	ACTION_MODE CHAR(1) NOT NULL DEFAULT '',
	MAP CHAR(1) NOT NULL DEFAULT '',
	LOCATION_X INT NOT NULL DEFAULT 0,
	LOCATION_Y INT NOT NULL DEFAULT 0,
	RACK_ID NVARCHAR(20) NOT NULL DEFAULT '',
	ERR_CODE INT NOT NULL  DEFAULT 0,
	LAST_TIME DATETIME NOT NULL DEFAULT GETDATE(),

	CONSTRAINT AGV_STATE_PK PRIMARY KEY(AGV_ID))

CREATE INDEX
	AGV_STATE_INDEX01 ON AGV_STATE
	(ORDER_NO, ORDER_SUB_NO)
CREATE TABLE STATION_INFO(

	ST_ID NVARCHAR(20) NOT NULL DEFAULT '',
	ST_NAME NVARCHAR(100) NOT NULL DEFAULT '',
	ST_TYPE INT NOT NULL DEFAULT 0,
	ST_ATTRIBUTE INT NOT NULL DEFAULT 0,
	MAKE_TIME DATETIME NOT NULL DEFAULT GETDATE(),
	LAST_TIME DATETIME NOT NULL DEFAULT GETDATE(),

	CONSTRAINT STATION_INFO_PK PRIMARY KEY(ST_ID))
CREATE TABLE AGV_ERR_HISTORY(
	
	ID INT IDENTITY(1,1) NOT NULL,
	FINISH INT NOT NULL  DEFAULT 0,
	MAKE_DATE CHAR(8) NOT NULL DEFAULT '',
	AGV_ID NVARCHAR(10) NOT NULL DEFAULT '',
	ERR_CODE INT NOT NULL DEFAULT 0,
	FLOOR_QR NVARCHAR(20) NOT NULL DEFAULT '',
	START_TIME DATETIME NOT NULL DEFAULT GETDATE(),
	END_TIME DATETIME DEFAULT NULL,
	MAKE_TIME DATETIME NOT NULL DEFAULT GETDATE(),
	LAST_TIME DATETIME NOT NULL DEFAULT GETDATE(),
	
	CONSTRAINT AGV_ERR_HISTORY_PK PRIMARY KEY(ID))

CREATE INDEX
	AGV_ERR_HISTORY_INDEX01 ON AGV_ERR_HISTORY
	(AGV_ID, ERR_CODE)

CREATE INDEX
	AGV_ERR_HISTORY_INDEX02 ON AGV_ERR_HISTORY
	(MAKE_DATE)
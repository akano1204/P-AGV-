CREATE TABLE AGV_ORDER(

	ID INT IDENTITY(1,1) NOT NULL,
	FINISH INT NOT NULL DEFAULT 0,
	RESPONSE INT NOT NULL DEFAULT 0,
	ORDER_NO INT NOT NULL DEFAULT 0,
	ORDER_SUB_NO INT NOT NULL DEFAULT 0,
	ORDER_MARK INT NOT NULL DEFAULT 0,
	AGV_ID NVARCHAR(10) NOT NULL DEFAULT '',
	ST_TO NVARCHAR(20) NOT NULL DEFAULT '',
	TO_QR NVARCHAR(20) NOT NULL DEFAULT '',
	ST_ACTION INT NOT NULL DEFAULT 0,
	RACK_ANGLE INT NOT NULL DEFAULT 0,
	SENSOR INT NOT NULL DEFAULT 0,
	MUSIC INT NOT NULL DEFAULT 0,
	ORDER_OP1 INT NOT NULL DEFAULT 0,
	ORDER_OP2 INT NOT NULL DEFAULT 0,
	ORDER_OP3 INT NOT NULL DEFAULT 0,
	ORDER_OP4 INT NOT NULL DEFAULT 0,
	ORDER_OP5 INT NOT NULL DEFAULT 0,
	O_INFO1 NVARCHAR(50) NOT NULL DEFAULT '',
	O_INFO2 NVARCHAR(50) NOT NULL DEFAULT '',
	O_INFO3 NVARCHAR(50) NOT NULL DEFAULT '',
	O_INFO4 NVARCHAR(50) NOT NULL DEFAULT '',
	O_INFO5 NVARCHAR(50) NOT NULL DEFAULT '',
	MAKE_TIME DATETIME NOT NULL DEFAULT GETDATE(),
	LAST_TIME DATETIME NOT NULL DEFAULT GETDATE(),

	CONSTRAINT AGV_ORDER_PK PRIMARY KEY(ID))

CREATE INDEX
	AGV_ORDER_INDEX01 ON AGV_ORDER
	(ORDER_NO, ORDER_SUB_NO)
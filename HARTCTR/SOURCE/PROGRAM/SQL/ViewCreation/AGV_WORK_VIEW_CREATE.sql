CREATE VIEW AGV_WORK_VIEW AS (SELECT

	AGV_WORK_HISTORY.ID,
	AGV_WORK_HISTORY.MAKE_DATE,
	AGV_WORK_HISTORY.ORDER_NO,
	AGV_WORK_HISTORY.ORDER_SUB_NO,
	AGV_ORDER.ORDER_MARK,
	AGV_WORK_HISTORY.AGV_ID,
	AGV_WORK_HISTORY.START_TIME,
	AGV_WORK_HISTORY.END_TIME,
	AGV_WORK_HISTORY.FROM_QR,
	AGV_WORK_HISTORY.TO_QR,
	AGV_ORDER.ST_TO,
	AGV_ORDER.ST_ACTION,
	AGV_WORK_HISTORY.RACK_ID,
	AGV_ORDER.RACK_ANGLE,
	AGV_ORDER.SENSOR,
	AGV_ORDER.MUSIC,
	AGV_ORDER.ORDER_OP1,
	AGV_ORDER.ORDER_OP2,
	AGV_ORDER.ORDER_OP3,
	AGV_ORDER.ORDER_OP4,
	AGV_ORDER.ORDER_OP5,
	AGV_ORDER.O_INFO1,
	AGV_ORDER.O_INFO2,
	AGV_ORDER.O_INFO3,
	AGV_ORDER.O_INFO4,
	AGV_ORDER.O_INFO5,
	AGV_WORK_HISTORY.MAKE_TIME,
	AGV_WORK_HISTORY.LAST_TIME

	FROM AGV_WORK_HISTORY

	LEFT JOIN AGV_ORDER ON
	AGV_WORK_HISTORY.ORDER_NO = AGV_ORDER.ORDER_NO AND
	AGV_WORK_HISTORY.ORDER_SUB_NO = AGV_ORDER.ORDER_SUB_NO)
CREATE VIEW AGV_ERR_VIEW AS (SELECT

	AGV_ERR_HISTORY.ID,
	AGV_ERR_HISTORY.FINISH,
	AGV_ERR_HISTORY.MAKE_DATE,
	AGV_ERR_HISTORY.AGV_ID,
	AGV_ERR_HISTORY.ERR_CODE,
	AGV_ERR_NAME.ERR_NAME,
	AGV_ERR_NAME.ERR_DETAIL,
	AGV_ERR_NAME.ERR_RECOVERY,
	AGV_ERR_HISTORY.FLOOR_QR,
	AGV_ERR_HISTORY.START_TIME,
	AGV_ERR_HISTORY.END_TIME,
	AGV_ERR_HISTORY.MAKE_TIME,
	AGV_ERR_HISTORY.LAST_TIME

	FROM AGV_ERR_HISTORY

	LEFT JOIN AGV_ERR_NAME ON
	AGV_ERR_HISTORY.ERR_CODE = AGV_ERR_NAME.ERR_CODE)